"""
演習1（forのネスト）
1から31までの整数を要素とするリスト1と，1から12までの整数を要素とするリスト2を作成します．
リスト1とリスト2から値を1つずつ取ってきた時，1の位が一致する値が何組あるかを求めてください．
例えば，1と11は，1の位が両方1なので，カウントに入ります．1と1，31と1なども同様です．

期待する出力：38
"""

list1,list2,count=list(range(1,32)),list(range(1,13)),0
for elem2 in list2:
  for elem1 in list1:
    if elem1%10==elem2%10:
      count+=1 
print(count)
