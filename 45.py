"""
45. ラムダ式
いま，辞書を要素とするリストが次のように与えられています．

li = [{'a': 6, 'b': 7, 'c': 6},
      {'a': 4, 'b': 2, 'c': 3},
      {'a': 1, 'b': 5, 'c': 8}]
このとき，辞書のキーが'b'の値に関して降順になるようにソートしてください．

期待する出力：[{'a': 6, 'b': 7, 'c': 6}, {'a': 1, 'b': 5, 'c': 8}, {'a': 4, 'b': 2, 'c': 3}]
"""

li = [{'a': 6, 'b': 7, 'c': 6},
      {'a': 4, 'b': 2, 'c': 3},
      {'a': 1, 'b': 5, 'c': 8}]
print(sorted(li,key=lambda x:x['b'],reverse=True))